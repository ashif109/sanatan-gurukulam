// Helper to secure-shuffle cards with cryptographically safe randomization
export function secureShuffleTarotDeck(): TarotCard[] {
  const array = [...tarotDeck];
  for (let i = array.length - 1; i > 0; i--) {
    // Cryptographically secure pseudorandom number generator if available
    let randomValue = 0;
    if (typeof window !== 'undefined' && window.crypto && window.crypto.getRandomValues) {
      const buffer = new Uint32Array(1);
      window.crypto.getRandomValues(buffer);
      randomValue = buffer[0] / (0xffffffff + 1);
    } else {
      randomValue = Math.random();
    }
    const j = Math.floor(randomValue * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}
